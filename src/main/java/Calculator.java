/**
 * Simple calculator for testing purpose
 *
 * @author (last commit) kirchhof
 * @version 1.0, 15.02.2019
 * @since 1.0
 */
public class Calculator {

  /**
   * Calculates the sum of two integers
   * @param a first summand
   * @param b second summand
   * @return sum
   */
  public int add (final int a, final int b) {
    return a+b;
  }

  /**
   * Subtracts an integer from another integer
   * @param a minuend, i.e. value from which b is subtracted
   * @param b subtrahend, i.e. value that is subtracted from a
   * @return difference
   */
  public int subtract (final int a, final int b) {
    return a-b;
  }
}

